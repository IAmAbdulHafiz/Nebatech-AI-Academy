    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const slides = document.querySelectorAll('.hero-slide');
            const slidesContainer = document.querySelector('.hero-slides');
            const prevBtn = document.getElementById('prevSlide');
            const nextBtn = document.getElementById('nextSlide');
            const indicators = document.querySelectorAll('.slide-indicator');
            
            let currentSlide = 0;
            const totalSlides = slides.length;
            
            // Auto-slide every 15 seconds
            let autoSlideInterval = setInterval(nextSlide, 15000);
            
            function updateSlider() {
                slidesContainer.style.transform = `translateX(-${currentSlide * 100}%)`;
                
                // Update indicators
                indicators.forEach((indicator, index) => {
                    if (index === currentSlide) {
                        indicator.classList.add('active');
                        indicator.classList.remove('bg-white/30', 'hover:bg-white/50');
                        indicator.classList.add('bg-secondary');
                    } else {
                        indicator.classList.remove('active');
                        indicator.classList.remove('bg-secondary');
                        indicator.classList.add('bg-white/30', 'hover:bg-white/50');
                    }
                });
            }
            
            function nextSlide() {
                currentSlide = (currentSlide + 1) % totalSlides;
                updateSlider();
            }
            
            function prevSlide() {
                currentSlide = (currentSlide - 1 + totalSlides) % totalSlides;
                updateSlider();
            }
            
            // Reset auto-slide on manual navigation
            function resetAutoSlide() {
                clearInterval(autoSlideInterval);
                autoSlideInterval = setInterval(nextSlide, 15000);
            }
            
            // Event listeners
            nextBtn.addEventListener('click', () => {
                nextSlide();
                resetAutoSlide();
            });
            
            prevBtn.addEventListener('click', () => {
                prevSlide();
                resetAutoSlide();
            });
            
            indicators.forEach((indicator, index) => {
                indicator.addEventListener('click', () => {
                    currentSlide = index;
                    updateSlider();
                    resetAutoSlide();
                });
            });
            
            // Pause on hover
            const heroSlider = document.getElementById('heroSlider');
            heroSlider.addEventListener('mouseenter', () => {
                clearInterval(autoSlideInterval);
            });
            
            heroSlider.addEventListener('mouseleave', () => {
                autoSlideInterval = setInterval(nextSlide, 15000);
            });
        });
    </script>

